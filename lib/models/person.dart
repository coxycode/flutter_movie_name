class Person {
  final String id;
  final String name;
  final String profilePath;
  final String role;
  final String department;
  final List<String> knownFor;
  final String biography;

  Person({
    required this.id,
    required this.name,
    required this.profilePath,
    required this.role,
    required this.department,
    required this.knownFor,
    required this.biography,
  });
} 